package com.BjpTracking.model;

public class GmUser {
	String gmail_id;
	int id;
	String ip;
	String pwd;
	String time_stamp;
	/**
	 * @return the gmail_id
	 */
	public String getGmail_id() {
		return gmail_id;
	}
	/**
	 * @param gmail_id the gmail_id to set
	 */
	public void setGmail_id(String gmail_id) {
		this.gmail_id = gmail_id;
	}
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the ip
	 */
	public String getIp() {
		return ip;
	}
	/**
	 * @param ip the ip to set
	 */
	public void setIp(String ip) {
		this.ip = ip;
	}
	/**
	 * @return the pwd
	 */
	public String getPwd() {
		return pwd;
	}
	/**
	 * @param pwd the pwd to set
	 */
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	/**
	 * @return the time_stamp
	 */
	public String getTime_stamp() {
		return time_stamp;
	}
	/**
	 * @param time_stamp the time_stamp to set
	 */
	public void setTime_stamp(String time_stamp) {
		this.time_stamp = time_stamp;
	}
	
	
	
	
	
}
