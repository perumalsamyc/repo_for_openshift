package com.BjpTracking.model;

public class Village {
	
	 
	int block_id;
	 
	int village_id;
	 
	String village_name;

	/**
	 * @return the block_id
	 */
	public int getBlock_id() {
		return block_id;
	}

	/**
	 * @param block_id the block_id to set
	 */
	public void setBlock_id(int block_id) {
		this.block_id = block_id;
	}

	/**
	 * @return the village_id
	 */
	public int getVillage_id() {
		return village_id;
	}

	/**
	 * @param village_id the village_id to set
	 */
	public void setVillage_id(int village_id) {
		this.village_id = village_id;
	}

	/**
	 * @return the village_name
	 */
	public String getVillage_name() {
		return village_name;
	}

	/**
	 * @param village_name the village_name to set
	 */
	public void setVillage_name(String village_name) {
		this.village_name = village_name;
	}
 
	
	
	
	

}
